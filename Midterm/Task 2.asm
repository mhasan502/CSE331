ORG 100H

MOV AH, 1
INT 21H
MOV AH, 0
SUB AL, 48


MOV CL, AL
MOV AL, IMAGINE
MOV BL, CL
DIV BL

MUL CL

CMP AL, IMAGINE
JZ PRINT_YES
JMP PRINT_NO


PRINT_YES:
MOV AH, 9
MOV DX, OFFSET YES
INT 21H
JMP FINISH

PRINT_NO: 
MOV AH, 9
MOV DX, OFFSET NO
INT 21H
JMP FINISH
    
FINISH:
RET  


IMAGINE DB 32

YES DB 10,13,"Yes$"
NO DB 10,13,"No$"         