ORG 100H

INPUT1:
MOV AH, 1
INT 21H
MOV AH, 0
SUB AL, 48
MOV A, AL

INPUT2:
MOV AH, 1
INT 21H
MOV AH, 0
SUB AL, 48
MOV B, AL

INPUT3:
MOV AH, 1
INT 21H
MOV AH, 0
SUB AL, 48
MOV C, AL

MOV AL, A
MOV BL, B
CMP AL, BL
JL CheckB
JMP CheckC

CheckB:
MOV AL, BL
MOV BL, C
CMP AL, BL
JG PrintB

CheckC:
MOV AL, A
MOV BL, C
CMP AL, BL
JL PrintC
JMP PrintA

PrintA:
MOV AH,9
MOV DX,OFFSET MSG1
INT 21H
JMP FINISH

PrintB:
MOV AH,9
MOV DX,OFFSET MSG2
INT 21H
JMP FINISH

PrintC:
MOV AH,9
MOV DX,OFFSET MSG3
INT 21H
JMP FINISH

FINISH:
RET

A DB 0
B DB 0
C DB 0
MSG1 DB 10,13,"A $"
MSG2 DB 10,13,"B $" 
MSG3 DB 10,13,"C $"