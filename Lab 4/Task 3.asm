ORG 100H

MOV AH, 1
INT 21H

CMP AL, 'A'
JGE CHECK1
JL INVALID

CHECK1:
CMP AL, 'Z'
JG CHECK2
MOV AH, 9
MOV DX, OFFSET UP
INT 21H
JMP FINISH

CHECK2:
CMP AL, 'a'
JL INVALID
JGE CHECK3

CHECK3:
CMP AL,'z'
JG INVALID
MOV AH, 9
MOV DX, OFFSET LO
INT 21H
JMP FINISH

INVALID:
MOV AH, 9
MOV DX, OFFSET INV
INT 21H
JMP FINISH


FINISH:
RET     

INV DB "INVALID $"
