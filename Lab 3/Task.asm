ORG 100H

LEA SI,ARR
MOV CX, 5 

SEARCH_LOOP:
MOV AL,[SI]
CMP AL,KEY
JZ FOUND
INC SI
LOOP SEARCH_LOOP
JMP NOTFOUND      

FOUND:
MOV AH,9
MOV DX,OFFSET MSG1
INT 21H
JMP FINISH 
 
NOTFOUND:
MOV AH,9
MOV DX,OFFSET MSG2
INT 21H
JMP FINISH 
 
FINISH:
RET

ARR DB 1,2,3,4,5
KEY DB 3
MSG1 DB "Found$"
MSG2 DB "Not Found$"
